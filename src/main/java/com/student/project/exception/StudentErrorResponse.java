package com.student.project.exception;

public class StudentErrorResponse {
   
private  int studentErrorCode;
	
	private  String studentErrorMessage;

	public int getStudentErrorCode() {
		return studentErrorCode;
	}

	public void setStudentErrorCode(int studentErrorCode) {
		this.studentErrorCode = studentErrorCode;
	}

	public String getStudentErrorMessage() {
		return studentErrorMessage;
	}

	public void setStudentErrorMessage(String studentErrorMessage) {
		this.studentErrorMessage = studentErrorMessage;
	}

	
}

	